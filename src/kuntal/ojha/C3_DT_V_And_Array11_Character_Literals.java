package kuntal.ojha;

public class C3_DT_V_And_Array11_Character_Literals {
    public static void main(String[] args) {
        System.out.print("\\Use of 377: ");
        System.out.println("\377");
        System.out.println("\u0061");
        System.out.println("\ua432");
        System.out.println("\'");
        System.out.println("\"");
        System.out.println("\\");
        System.out.println("Use of: \r\n");
        System.out.println("My Name is:\nKuntal Ojha.");
        System.out.println("\nMy name is:\fKuntal Ojha.");
        System.out.println("My name is:\tKuntal Ojha.\n");
        System.out.println("WOW\b");
    }
}
