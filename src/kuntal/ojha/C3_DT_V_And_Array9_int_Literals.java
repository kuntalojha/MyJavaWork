public class C3_DT_V_And_Array9_int_Literals {
    public static void main(String[] args) {
        int x=0b1010;
        int x1=123_456_789;
        int x2=123__456__789;
        int x3=0b1101_0101_0001_1010;
        System.out.println("\nint x=0b1010;\n" +x+"\n"+
                "\nint x1=123_456_789;\n" +x1+"\n"+
                "\nint x2=123__456__789;\n" +x2+"\n"+
                "\nint x3=0b1101_0101_0001_1010;\n"+x3);
    }
}
