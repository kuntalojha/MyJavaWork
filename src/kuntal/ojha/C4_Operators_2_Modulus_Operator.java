package kuntal.ojha;
// Demonstrate the % operator
public class C4_Operators_2_Modulus_Operator {
    public static void main(String[] args) {
        int x=42;
        double y=42.25;
        double z=390.0291;
        System.out.println("X modulus 10 = "+ x%10);
        System.out.println("Y modulus 10 = "+ y%10);
        System.out.println("Z modulus 10 = "+ z%10);
    }
}
