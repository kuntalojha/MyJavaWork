package kuntal.ojha;

// Declare a loop control variable inside the for
public class C5_Control_Statements_19_For_Loop {
    public static void main(String[] args) {
        // Here, n is declared inside of the for loop.
        for (int n = 10; n >0; n--) {
            System.out.println("My name is Kuntal Ojha. & the value of i="+n);
        }
    }
}
