package kuntal.ojha;

// Using the comma
public class C5_Control_Statements_21_For_Loop {
    public static void main(String[] args) {
        int a,b;
        b=4;
        for (a=1;a<b;a++){
            System.out.println("a= "+a);
            System.out.println("b= "+b);
            b--;
        }
    }
}
